package com.challenge.backend.settlement.entity;

/**
 * 정산 방식 구분.
 * 챌린지 종료 시 완주자·미완주자 분포에 따라 자동으로 결정됨.
 */
public enum SettlementType {
    /**
     * 일반 정산 — 완주자와 미완주자가 모두 존재하는 경우.
     * 미완주자의 보증금을 모아서 완주자에게 N분배.
     */
    NORMAL,
    /**
     * 전액 환불 — 아래 3가지 경우 모두 이 타입으로 처리.
     * 1) 전원 완주: 나눠줄 미완주자 보증금이 없으므로 각자 원금 환불
     * 2) 전원 실패: 받을 완주자가 없으므로 각자 원금 환불
     * 3) 전원 동의 취소: 챌린지 중단으로 각자 원금 환불
     */
    FULL_REFUND
}
